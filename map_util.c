#include "map.h"

int	ft_strlen_no_line(const char *s)
{
	int	i;

	if (*s == '\0' || s == NULL)
		return (0);
	i = 0;
	while (s[i] && s[i] != '\n')
	{
		i++;
	}
	return (i);
}
